import { type NextPage } from "next";
import Head from "next/head";

import { api } from "../../../utils/api";
import { useRouter } from "next/router";
import Favorite from "../../../components/Favorites/Favorite";
import Layout from "../../../components/Layout";

import Image from "next/image";
import MinimalCommentCard from "../../../components/MinimalCommentCard";
import { RestaurantCardSkeleton } from "../../restaurant";
const User: NextPage = () => {
  const router = useRouter();
  const { userId } = router.query;

  const user = api.user.getUserPageData.useQuery({ id: String(userId) });

  const favorites = api.favorite.getByUserId.useQuery({ id: String(userId) });
  const comments = api.comment.getByUserId.useQuery({ id: String(userId) });
  return (
    <>
      <Head>
        <title>Foodley</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <section className="py-2 md:py-4">
          <div className="mx-auto  max-w-3xl px-4  lg:px-8">
            <div className="flex flex-col gap-2 md:flex-row">
              <div className="avatar">
                <div className="w-24 rounded border-2 border-zinc-600">
                  <Image
                    width={60}
                    height={60}
                    src={user.data?.image || ""}
                    alt="Profile Image"
                  />
                </div>
              </div>

              <h3 className="text-xl font-bold text-primary ">
                {user.data?.username}
              </h3>
            </div>
            <div className="divider-primary divider"></div>
            <div className="flex flex-col gap-2">
              {favorites.isLoading && (
                <div>
                  {new Array(5).fill(true).map((elem, index) => (
                    <RestaurantCardSkeleton key={index} />
                  ))}
                </div>
              )}
              <div className="text-lg font-bold">Top 5</div>
              {favorites.data?.slice(0, 5).map((elem) => {
                return (
                  <Favorite
                    key={elem.id}
                    restaurantId={elem.restaurant_id}
                    disableClick={false}
                  />
                );
              })}{" "}
              <div className=" flex flex-col gap-2 px-4">
                <h3 className="text-lg font-bold">
                  {comments.data?.length} Reviews
                </h3>
                {comments.isLoading && <div>Loading...</div>}
                {comments.data?.length === 0 && (
                  <div
                    className="
                  text-primary md:text-3xl
                  "
                  >
                    No comments yet. Be the first to comment!
                  </div>
                )}

                {comments.data?.map((comment) => (
                  <MinimalCommentCard comment={comment} viewOnly />
                ))}
              </div>
            </div>
          </div>
        </section>
      </Layout>
    </>
  );
};

export default User;
